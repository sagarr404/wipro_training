package com.pack1;

public class Calculator {

	public int addition(int a, int b) {
		return a+b;
	}
	
	public int multiply ( int a, int b) {
		return a*b;
	}
	
	public int subtraction( int a, int b) {
		return a-b;
	}
	
	public int division( int a, int b) throws ArithmeticException {
		return a/b;
	}
}
